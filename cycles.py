# Найти произведение положительных, сумму и количество отрицательных из 10 введенных целых значений
numbers = [int(input("Введите число: ")) for i in range(10)]

count_negative = 0
sum_negative = 0
product = 1
has_positive = False

for i in numbers:
    if i < 0:
        sum_negative += i
        count_negative += 1
    elif i > 0:
        product *= i
        has_positive = True

print(f'Сумма отрицательных чисел {sum_negative}, количество отрицательных чисел {count_negative}')
print(f'Произведение положительных чисел {product}') if has_positive else print('Положительных чисел не было')


